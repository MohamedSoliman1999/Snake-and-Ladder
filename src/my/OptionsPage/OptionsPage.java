    private void Sound()
   {
        try{
                  FileReader reader = new FileReader("Game.txt");
                  String st;
            try (BufferedReader bf = new BufferedReader(reader)) {
                st=bf.readLine();
            }
            switch (st) {
                case "true":
                    sound = true;
                    SoundBTN.setIcon(new ImageIcon(getClass().getResource("/my/OptionsPage/On button.png")));
                    break;
                case "false":
                    sound = false;
                    SoundBTN.setIcon(new ImageIcon(getClass().getResource("/my/OptionsPage/Off button.png")));
                    break;
            }         
              }
           catch(Exception ex){
               JOptionPane.showMessageDialog(null, ex);
           }
   }
    
