    private void SoundBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SoundBTNActionPerformed
       if ((SoundBTN.isSelected() && sound == true) || (!SoundBTN.isSelected() && sound == true) )
      {
          SoundBTN.setIcon(new ImageIcon(getClass().getResource("/my/OptionsPage/Off button.png")));
          sound = false;
            try{
                 FileWriter writer = new FileWriter("Game.txt");
                 PrintWriter pwriter = new PrintWriter(writer);
                 pwriter.println("false");
                 pwriter.close();
             }
             catch(Exception ex){
                 JOptionPane.showMessageDialog(null, ex);
               }
      }
      else if(SoundBTN.isSelected() && sound == false || (!SoundBTN.isSelected() && sound == false))
      {
          SoundBTN.setIcon(new ImageIcon(getClass().getResource("/my/OptionsPage/On button.png")));
          sound = true;
           try{
                 FileWriter writer = new FileWriter("Game.txt");
                 PrintWriter pwriter = new PrintWriter(writer);
                 pwriter.println("true");
                 pwriter.close();
             }
             catch(Exception ex){
                 JOptionPane.showMessageDialog(null, ex);
               }
      }
        
    }//GEN-LAST:event_SoundBTNActionPerformed

